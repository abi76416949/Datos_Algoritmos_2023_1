#Comparar que dos números sean iguales, diferentes, que el primer número sea mayor que
#el segundo valor y que el primer valor sea menor o igual que el segundo valor. 

numero1 = float(input("Ingrese el primer número: "))
numero2 = float(input("Ingrese el segundo número: "))

# Comparar si los números son iguales
print("Los números son iguales:", numero1 == numero2)

# Comparar si los números son diferentes
print("Los números son diferentes:", numero1 != numero2)

# Comparar si el primer número es mayor que el segundo
print("El primer número es mayor que el segundo:", numero1 > numero2)

# Comparar si el primer número es menor o igual que el segundo
print("El primer número es menor o igual que el segundo:", numero1 <= numero2)